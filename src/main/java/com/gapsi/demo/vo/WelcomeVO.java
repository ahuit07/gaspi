package com.gapsi.demo.vo;


public class WelcomeVO {
	private String title;
	private String titleFooter;
	private String titleWelcome;
	private String titleWelcomeName;
	private String titleVersion;
	private String titleWelcomeButton;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public String getTitleFooter() {
		return titleFooter;
	}
	public void setTitleFooter(String titleFooter) {
		this.titleFooter = titleFooter;
	}
	public String getTitleWelcome() {
		return titleWelcome;
	}
	public void setTitleWelcome(String titleWelcome) {
		this.titleWelcome = titleWelcome;
	}
	public String getTitleWelcomeName() {
		return titleWelcomeName;
	}
	public void setTitleWelcomeName(String titleWelcomeName) {
		this.titleWelcomeName = titleWelcomeName;
	}
	public String getTitleVersion() {
		return titleVersion;
	}
	public void setTitleVersion(String titleVersion) {
		this.titleVersion = titleVersion;
	}
	public String getTitleWelcomeButton() {
		return titleWelcomeButton;
	}
	public void setTitleWelcomeButton(String titleWelcomeButton) {
		this.titleWelcomeButton = titleWelcomeButton;
	}
}
